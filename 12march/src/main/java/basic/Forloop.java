package basic;

public class Forloop {
	public static void main(String[] args) {
		
		for (int a=1; a<=4; a++)
		{
		for (int b=1; b<=4; b++) 
		{
		System.out.print(b);//1 2 3 4
		
		}
		System.out.println();
		}
		
	}

}
